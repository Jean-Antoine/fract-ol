#include "fractal.h"

t_cmplx ft_julia(t_cmplx z, t_cmplx c)
{
    t_cmplx res;

    res.r = z.r * z.r - z.i * z.i + c.r;
    res.i = 2 * z.r * z.i + c.i;
    return (res);
}
